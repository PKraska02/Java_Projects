public class IntegerDoublePair {
    private Integer key;
    private Double value;

    public IntegerDoublePair(Integer key, Double value) {
        this.key = key;
        this.value = value;
    }

    public Integer getKey() {
        return key;
    }

    public Double getValue() {
        return value;
    }

    public void setValue(Double value) {
        this.value = value;
    }
}
